package semi.vo;

public class InquiryCategory {
	private int no;
	private String name;
	
	public InquiryCategory() {
	
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
